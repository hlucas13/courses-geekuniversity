// Operador unário e Operação ternária

// Unário
var idade = 25;
idade++; // operação unária

console.log(idade);

// Ternário
var sexo = 'm';

if (sexo === 'm') {
    console.log('Masculino');
} else {
    console.log('Feminino');
}

// Operador ternário (funciona somente com if else simples)
console.log(sexo === 'm' ? 'Masculino' : 'Feminino');
